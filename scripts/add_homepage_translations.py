def set_nested_value(data, path, value):
    """Helper function to set nested dictionary values using dot notation"""
    keys = path.split('.')
    for key in keys[:-1]:
        if key not in data:
            data[key] = {}
        data = data[key]
    data[keys[-1]] = value

